#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;

	if (ac == 2)
	{
		i = 0;
		while (av[1][i])
		{
			if ((av[1][i] >= 9 && av[1][i] <= 13) || (av[1][i] == 32))
				i++;
			if ((av[1][i] >= 'a' && av[1][i] <= 'z') ||
				(av[1][i] >= 'A' && av[1][i] <= 'Z') ||
				(av[1][i] >= '0' && av[1][i] <= '9'))
				write(1, &av[1][i], 1);
			if ((av[1][i] >= 9 && av[1][i] <= 13) || (av[1][i] == 32))
				break ;
			i++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
