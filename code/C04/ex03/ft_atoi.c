int	ft_atoi(char *str)
{
	int	i;
	int	mn;
	int	nbr;

	mn = 0;
	nbr = 0;
	i = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			mn++;
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		nbr *= 10;
		nbr += str[i] - 48;
		i++;
	}
	if (! (mn % 2))
		return (nbr);
	return (-nbr);
}
