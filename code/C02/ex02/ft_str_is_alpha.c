int	ft_str_is_alpha(char *str)
{
	int	i;
	int	is_alpha;

	i = 0;
	while (str[i])
	{
		if ((str[i] >= 65 && str[i] <= 90) || (str[i] >= 97 && str[i] <= 122))
			is_alpha = 1;
		else
			return (0);
		i++;
	}
	return (is_alpha);
}
